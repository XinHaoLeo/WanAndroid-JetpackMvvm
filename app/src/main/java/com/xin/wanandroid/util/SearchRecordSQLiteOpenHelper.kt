class SearchRecordSQLiteOpenHelper(context: Context?) :
    SQLiteOpenHelper(
        context,
        DB_NAME,
        null,
        DB_VERSION
    ) {
    override fun onCreate(db: SQLiteDatabase) {
        val sqlStr =
            "CREATE TABLE searchRecord (_id INTEGER PRIMARY KEY AUTOINCREMENT, username TEXT, keyword TEXT, time NOT NULL DEFAULT (datetime('now','localtime')));"
        db.execSQL(sqlStr)
    }

    override fun onUpgrade(
        db: SQLiteDatabase,
        oldVersion: Int,
        newVersion: Int
    ) {
    }

    companion object {
        private const val DB_NAME = "search_record.db"
        private const val DB_VERSION = 1
    }
}