class ImageLoader :ImageLoader() {
    override fun displayImage(context: Context, path: Any?, imageView: ImageView) {
        Glide.with(context).load(path).into(imageView)
        val uri = Uri.parse(path as String?)
        imageView.setImageURI(uri)
    }
}