class PersonRankAdapter : BaseQuickAdapter<DataXX, BaseViewHolder>(R.layout.item_person_rank) {

    override fun convert(holder: BaseViewHolder, item: DataXX) {
        holder.itemView.apply {
            tvScoreType.text = item.reason
            tvScoreTime.text = TimeUtils.millis2String(item.date)
            val coinCount = "+${item.coinCount}"
            tvPlusScore.text = coinCount
        }
    }
