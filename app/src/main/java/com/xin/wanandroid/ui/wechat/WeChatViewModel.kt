class WeChatViewModel : BaseViewModel() {

    val isReload: MutableLiveData<Boolean> = MutableLiveData()
    val weChatData: MutableLiveData<MutableList<WeChatData>> = MutableLiveData()

    fun getWeChatData() {
        isReload.value = false
        request(
            {
                weChatData.value = mApiRepository.getWeChatData()
            }, {
                isReload.value = weChatData.value.isNullOrEmpty()
            }
        )
    }

}